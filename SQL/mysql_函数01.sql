mysql 函数包括数学函数、字符串函数、日期和时间函数、条件判断函数、
系统信息函数、加密函数、格式化函数等。


[ 数字函数 ]
表：
abs(x)						返回x的绝对值
ceil(x),ceiling(x)			返回大于或等于x的最小整数
floor(x)					返回小于或等于x的最大整数
rand()						返回0-1的随机数
rand(x)						返回0-1的随机数，x值相同时返回的随机数相同
sign(x)						返回x的符号，x是负数、0、正数分别返回-1、0、1
pi()						返回圆周率（3.141593）
truncate(x,y)				返回数值x保留到小数点后y位的值
round(x)					返回离x最近的整数
round(x,y)					保留x小数点后y位的值，但截取时要进行四舍五入
pow(x,y),power(x,y) 		返回x的y次方
sqrt(x)						返回x的平方根
exp(x)						返回e的x次方
mod(x,y)					返回x除以y以后的余数
log(x)						返回自然对数（以e为底的对数）
log10(x)					返回以10为底的对数
radians(x)					将角度转换为弧底
degrees(x)					将弧底转换为角度
sin(x)						求正弦值
asin(x)						求反正弦值
cos(x)						求余弦值
acos(x)						求反余弦值
tan(x)						求正切值
atan(x),atan2(x,y)			求反正切值
cot(x)						求余切值


