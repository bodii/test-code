#include <stdio.h>

/*
 * ---------------
 *   函数头
 *   ------
 *
 *   int main(void)
 * ----------------
 *
 * 
 * ---------------
 *   函数体
 * ---------------
 *  {
 *     int q;  // 声明
 *     q = 1;  // 赋值
 *     printf('%d is neat. \n", q); // 语句
 *     return 0; // 结束返回
 *  }
 * ---------------
 */


int main(void)
{
	int q;
	q = 1;

	printf("%d is neat. \n", q);

	return 0;
}
