/*
	理解行为型设计模式的关键是 【通信】。对于这些模式，重点不
	再是构成一个设计模式的对象和类，而应转变为对象和类之间的通信。
	实际上，最好考虑对象如何相互合作来完成任务，从这个角度来理解
	行为型设计模式。

	重点考虑构成模式的元素之间的交互，这一点非常重要，有些模式的类图
	看起来是一样的，如状态模式和策略模式。不过，由于参与者通信的方式
	不同，另外它们处理职责的方式也不同，所以这些模式实际上有很大不同。
	

 */